# 4.24 Implement function cheer() that takes as input a team name (as a string) and prints
# a cheer 

def cheer(team_name):
    print("How do you spell winner? ")
    print("I know, I know!")
    print(f"{team_name.upper()} !")
    print("And that's how you spell winner!")
    print(f"Go {team_name.title()}!")

cheer('Huskies')